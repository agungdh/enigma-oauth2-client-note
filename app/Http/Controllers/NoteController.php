    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $request->validate([
            'title' => 'required',
            'note' => 'required',
        ]);

        $note = new Note();

        $note->user_id = session('user.id');
        $note->title = $request->title;
        $note->note = $request->note;
        $note->date_time = now();

        $note->save();
    }

